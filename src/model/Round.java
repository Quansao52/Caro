package model;

import java.io.Serializable;

/**
 *
 * @author Lenovo
 */
public class Round implements Serializable {
    private int id;
    private String name;
    public Round(){
        super();
    }

    public Round( String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
